# 2. ІНСТРУМЕНТ КОНВЕРТАЦІЇ ДЛЯ США (Localizer US)
# =====================================================================

class SingleConversion(BaseModel):
    """Одна одиниця для конвертації."""
    value: float = Field(..., description="Numerical value to convert (e.g., 300.5)")
    unit: str = Field(..., description="Metric unit: 'mm', 'cm', or 'kg'")
    label: Optional[str] = Field(
        default=None,
        description="Optional label for context (e.g., 'Width', 'Weight')"
    )


class ConversionInput(BaseModel):
    """Вхідна схема — підтримує як одне, так і кілька значень."""
    conversions: List[SingleConversion] = Field(
        ...,
        description=(
            "List of values to convert. Example: "
            "[{\"value\": 300, \"unit\": \"mm\", \"label\": \"Width\"}, "
            "{\"value\": 14.5, \"unit\": \"kg\", \"label\": \"Weight\"}]"
        ),
        min_length=1,
        max_length=20
    )


class USMeasurementCalculatorTool(BaseTool):
    name: str = "US Measurement Calculator"
    description: str = (
        "Calculates exact Imperial conversions for multiple values at once. "
        "Supports: mm→inches, cm→inches, kg→lbs. "
        "Returns formatted strings like '11.81\" (300 mm)' or '31.97 lbs (14.5 kg)'. "
        "Use STRICTLY for: Printer Dimensions, Build Volume, and Weight. "
        "Do NOT use for: Nozzle diameter, Filament diameter, Temperatures, or Print Speed. "
        "Pass ALL values in a single call using the 'conversions' array to avoid multiple tool calls."
    )
    args_schema: type[BaseModel] = ConversionInput

    # Коефіцієнти конвертації
    CONVERSION_FACTORS: ClassVar[Dict] = {
        'mm': {'factor': 1 / 25.4, 'imperial_unit': '"', 'metric_unit': 'mm'},
        'cm': {'factor': 1 / 2.54, 'imperial_unit': '"', 'metric_unit': 'cm'},
        'kg': {'factor': 2.20462, 'imperial_unit': ' lbs', 'metric_unit': 'kg'},
    }

    def _format_number(self, val: float) -> str:
        """Форматує число: 11.0 → '11', 11.81 → '11.81'."""
        return f"{int(val)}" if val == int(val) else f"{val}"

    def _convert_single(self, value: float, unit: str, label: Optional[str]) -> str:
        """Конвертує одне значення."""
        unit = str(unit).strip().lower()

        try:
            val = float(value)
        except (ValueError, TypeError):
            prefix = f"{label}: " if label else ""
            return f"{prefix}Error — invalid numerical value '{value}'"

        conv = self.CONVERSION_FACTORS.get(unit)
        if not conv:
            prefix = f"{label}: " if label else ""
            return (
                f"{prefix}Error — unsupported unit '{unit}'. "
                f"Only {list(self.CONVERSION_FACTORS.keys())} are allowed."
            )

        imperial_val = round(val * conv['factor'], 2)
        result = (
            f"{self._format_number(imperial_val)}{conv['imperial_unit']} "
            f"({self._format_number(val)} {conv['metric_unit']})"
        )

        if label:
            return f"{label}: {result}"
        return result

    def _run(self, conversions: list) -> str:
        """Обробляє batch конвертацій."""
        # Захист: LLM іноді передає dict замість list
        if isinstance(conversions, dict):
            conversions = [conversions]

        results = []
        for item in conversions:
            if isinstance(item, dict):
                val = item.get('value', 0)
                unit = item.get('unit', '')
                label = item.get('label')
            elif isinstance(item, SingleConversion):
                val = item.value
                unit = item.unit
                label = item.label
            else:
                results.append(f"Error — unexpected input format: {type(item)}")
                continue

            results.append(self._convert_single(val, unit, label))

        return "\n".join(results)